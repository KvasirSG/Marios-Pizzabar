package PizzaUI;

/**
 * Defines the types of buttons used in the Pizza application UI, indicating their intended action.
 */
public enum ButtonType {
    PIZZA, // Button for adding a pizza to an order
    RPIZZA, // Button for removing a pizza from an order
    CORDER, // Button for completing an order
    RORDER, // Button for removing an order
    AORDER, // Button associated with an active order
    NONE // No button
}
